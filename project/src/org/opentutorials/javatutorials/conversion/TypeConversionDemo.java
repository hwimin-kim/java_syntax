package org.opentutorials.javatutorials.conversion;

public class TypeConversionDemo {

	public static void main(String[] args) {
		
		/* byte -> short -> int -> long -> float -> double
		            byte -> int                            
		            (형변환 순서)                               */
		int a = 3;
		float b = 1.0F;
		double c = a + b; // double c = 3.0F + 1.0F

	}

}
